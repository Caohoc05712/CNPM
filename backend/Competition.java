public class Competition {
    private int id;
    private String name;
    private String startDate;
    private String endDate;
    private String status;

    public Competition(int id, String name, String startDate, String endDate, String status) {
        this.id = id;
        this.name = name;
        this.startDate = startDate;
        this.endDate = endDate;
        this.status = status;
    }
}
